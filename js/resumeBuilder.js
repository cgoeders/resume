//Modify punctuation of name (internationalize)
function inName(nameold) {
    var spacePos = nameold.indexOf(" ");
    var name1end = nameold.substr(1, spacePos);
    var name1beg = nameold.charAt([0]).toUpperCase();
    var name1new = name1beg + name1end;
    var name2new = nameold.substr(spacePos+1).toUpperCase();
    return name1new + name2new;
}
